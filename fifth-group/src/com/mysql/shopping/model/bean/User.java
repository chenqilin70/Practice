package com.mysql.shopping.model.bean;

import java.util.Set;

public class User {

	private int UserID;
	private String Username;
	private String Password;
	private String Realname;
	private String sex;
	private int Age;
	private String image;
	private boolean status;
	private  Set<Address>  adds;
	
	public Set<Address> getAdds() {
		return adds;
	}

	public void setAdds(Set<Address> adds) {
		this.adds = adds;
	}

	public User() {
		super();
	}

	public User(int userID, String username, String password, String realname, String sex, int age, String image,
			boolean status) {
		super();
		UserID = userID;
		Username = username;
		Password = password;
		Realname = realname;
		this.sex = sex;
		Age = age;
		this.image = image;
		this.status = status;
	}

	public int getUserID() {
		return UserID;
	}

	public void setUserID(int userID) {
		UserID = userID;
	}

	public String getUsername() {
		return Username;
	}

	public void setUsername(String username) {
		Username = username;
	}

	public String getPassword() {
		return Password;
	}

	public void setPassword(String password) {
		Password = password;
	}

	public String getRealname() {
		return Realname;
	}

	public void setRealname(String realname) {
		Realname = realname;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public int getAge() {
		return Age;
	}

	public void setAge(int age) {
		Age = age;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public boolean isStatus() {
		return status;
	}

	public void setStatus(boolean status) {
		this.status = status;
	}
	


}
