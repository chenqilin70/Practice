package admin.com.oracle.model.bean;

public class Limitbean {
	private int limitid;
	private String limitaction;
	private String limitcontrol;
	public int getLimitid() {
		return limitid;
	}
	public void setLimitid(int limitid) {
		this.limitid = limitid;
	}
	public String getLimitaction() {
		return limitaction;
	}
	public void setLimitaction(String limitaction) {
		this.limitaction = limitaction;
	}
	public String getLimitcontrol() {
		return limitcontrol;
	}
	public void setLimitcontrol(String limitcontrol) {
		this.limitcontrol = limitcontrol;
	}
	public Limitbean(int limitid, String limitaction, String limitcontrol) {
		super();
		this.limitid = limitid;
		this.limitaction = limitaction;
		this.limitcontrol = limitcontrol;
	}
	public Limitbean() {
		super();
	}
	@Override
	public String toString() {
		return "Limitbean [limitid=" + limitid + ", limitaction=" + limitaction + ", limitcontrol=" + limitcontrol
				+ "]";
	}

	
}
