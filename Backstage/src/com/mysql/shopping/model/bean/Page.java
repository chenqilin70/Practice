package com.mysql.shopping.model.bean;

public class Page {
	//封装一个page对象
	private int count;
	private int Nowpage;
	private int Firstpage;
	private int Prepage;
	private int Nextpage;
	private int Lastpage;
	private int Allcount;
	
	public Page() {
		super();
	}
	public Page(int count, int nowpage, int firstpage, int prepage, int nextpage, int lastpage, int allcount) {
		super();
		this.count = count;
		Nowpage = nowpage;
		Firstpage = firstpage;
		Prepage = prepage;
		Nextpage = nextpage;
		Lastpage = lastpage;
		Allcount = allcount;
	}
	@Override
	public String toString() {
		return "Page [count=" + count + ", Nowpage=" + Nowpage + ", Firstpage=" + Firstpage + ", Prepage=" + Prepage
				+ ", Nextpage=" + Nextpage + ", Lastpage=" + Lastpage + ", Allcount=" + Allcount + "]";
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getNowpage() {
		return Nowpage;
	}
	public void setNowpage(int nowpage) {
		Nowpage = nowpage;
	}
	public int getFirstpage() {
		return Firstpage;
	}
	public void setFirstpage(int firstpage) {
		Firstpage = firstpage;
	}
	public int getPrepage() {
		return Prepage;
	}
	public void setPrepage(int prepage) {
		Prepage = prepage;
	}
	public int getNextpage() {
		return Nextpage;
	}
	public void setNextpage(int nextpage) {
		Nextpage = nextpage;
	}
	public int getLastpage() {
		return Lastpage;
	}
	public void setLastpage(int lastpage) {
		Lastpage = lastpage;
	}
	public int getAllcount() {
		return Allcount;
	}
	public void setAllcount(int allcount) {
		Allcount = allcount;
	}
	
	
}
